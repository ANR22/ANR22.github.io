const assetBody = (asset) => {
  const { asset_type } = asset;

  if (asset.asset_content == "tb") {
    return '<img src="./tb.png" class="tb" alt="tb" />';
  } else if (asset.asset_content == "eb") {
    return '<img src="./eb.png" class="eb" alt="eb" />';
  } else if (asset.display_asset_video) {
    return `<iframe class='video' src=${asset.display_asset_video}></iframe>`;
  } else if (asset.display_asset_docs) {
    return `<div class='docs'><iframe src=${asset.display_asset_docs}></iframe></div>`;
  } else if (asset.display_asset_image) {
    return `<img class='image' src=${asset.display_asset_image} />`;
  } else if (asset.display_asset_url) {
    return `<iframe class='audio' src=${asset.display_asset_url}></iframe>`;
  } else if (
    asset.asset_content == "reflection" &&
    asset_type == "input_asset"
  ) {
    return `<div class='article'><textarea rows='10'} placeholder='Reflect as per guidelines'></textarea></div>`;
  } else if (asset.asset_content == "article") {
    return `<div class='article'><textarea rows='10' placeholder='article'></textarea><button>Submit</button></div>`;
  } else if (asset.display_asset_reflection) {
    return `<p class='display-refelction'>${asset.display_asset_reflection}</p>`;
  }
};

